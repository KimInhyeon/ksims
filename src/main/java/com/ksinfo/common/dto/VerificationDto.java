package com.ksinfo.common.dto;

public class VerificationDto {

	private String mumberNum;
	private String numberConfirm;
	private String mumberPass;
	private String userPassword;
	
	public String getMumberNum() {
		return mumberNum;
	}
	public void setMumberNum(String mumberNum) {
		this.mumberNum = mumberNum;
	}
	public String getNumberConfirm() {
		return numberConfirm;
	}
	public void setNumberConfirm(String numberConfirm) {
		this.numberConfirm = numberConfirm;
	}
	public String getMumberPass() {
		return mumberPass;
	}
	public void setMumberPass(String mumberPass) {
		this.mumberPass = mumberPass;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}

	
	
}
